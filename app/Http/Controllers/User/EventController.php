     */
    public function store(Request $request)
    {
        if (is_array($request->actions) ) {

            foreach ($request->actions as $action) {
                $record = new Event();
                $record->user_id = Auth::user()->id;
                $record->action_id = $action;
                $record->save();
            }

        }
        return redirect()->back();
    }

